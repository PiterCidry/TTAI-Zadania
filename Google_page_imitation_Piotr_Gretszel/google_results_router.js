function changeActiveInactive(to, from) {
    if (!from) {
        document.getElementById(to).classList.remove("category-inactive");
        document.getElementById(to).classList.add("category-active");
    }
    else if (to) {
        document.getElementById(to).classList.remove("category-inactive");
        document.getElementById(from).classList.remove("category-active");
        document.getElementById(to).classList.add("category-active");
        document.getElementById(from).classList.add("category-inactive");
    }
}

